    private void DownloadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DownloadMouseClicked
        // TODO add your handling code here:
        int rowindex = courstable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) courstable.getModel();
        if(rowindex == -1){
            JOptionPane.showMessageDialog(null, "You must to choose a file", "Download failed", 2);
        }else{
           String ftitle = model.getValueAt(rowindex,0).toString();
           DB_Operation db = new DB_Operation();
           ResultSet rs = db.searchQuery("SELECT * FROM files WHERE title='"+ftitle+"'");
           try {
            if(rs.next()){
                File theFile = new File(rs.getString(1));
                output = new FileOutputStream(theFile);
                input = rs.getBinaryStream(2);
                byte[] buffer = new byte[1024];
                while(input.read(buffer) > 0){
                    output.write(buffer);
                }
                JOptionPane.showMessageDialog(null, "the files is saved in "+theFile.getAbsolutePath());
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        finally{
            if(input != null){
                try {
                    input.close();
                } catch (IOException ex) {
                    Logger.getLogger(UserPage.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(output != null){
                try {
                    output.close();
                } catch (IOException ex) {
                    Logger.getLogger(UserPage.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        }
        
    }//GEN-LAST:event_DownloadMouseClicked

